    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        modelTB.setColumnIdentifiers(columns);
        
        tbVenda.setModel(modelTB);
        
        Object[] row = new Object[3];
        row[0] = lblIum.getText();
        row[1] = lblNome.getText();
        row[2] = lblValor.getText();
        
        modelTB.addRow(row);
        
        valorTotal += Double.valueOf(lblValor.getText());
        
        
        if(desconto){
            
            valorCDesconto = valorTotal * 0.9;
            
            DecimalFormat formato = new DecimalFormat("#.##");      
            
            Double preco = Double.valueOf(formato.format(total));
            
            lblDesconto.setText(Double.toString((valorTotal * 0.1)));
            lblValorTotal.setText(Double.toString(valorCDesconto));
            calculaTotal();
        }else{
            lblValorTotal.setText(Double.toString(valorTotal));
            calculaTotal();
        }
        
    }//GEN-LAST:event_btnAdicionarActionPerformed

    private void btnDescontoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescontoActionPerformed
        // TODO add your handling code here:
        try {
        ModelClienteEspecial c = new ModelClienteEspecial();
        
        c.setCpf(txtCpf.getText());
        
        c = DAOcliente.buscar(c);
        
        if(c != null) { //achou
                //permitir desconto
                JOptionPane.showMessageDialog(this, 
                        "Desconto Autorizado!",
                        "Mensagem ao Usuário",
                        JOptionPane.INFORMATION_MESSAGE);
                valorCDesconto = valorTotal * 0.9;
                lblDesconto.setText(Double.toString((valorTotal * 0.1)));
                lblValorTotal.setText(Double.toString(valorCDesconto));
                desconto = true;
                txtCpf.setEnabled(false);
                calculaTotal();
            }
            else {
                JOptionPane.showMessageDialog(this, 
                        "Cliente não Encontrado!",
                        "Mensagem ao Usuário",
                        JOptionPane.INFORMATION_MESSAGE);
                //habilitaInicio();
                txtCpf.requestFocus();
                txtCpf.setText("");
                calculaTotal();
            }
        } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(this,
                        "Erro SQL " + ex.getMessage(),
                        "Mensagem ao Usuário",
                        JOptionPane.ERROR_MESSAGE);
            //Logger.getLogger(GerenciamentoCliente.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FormVenda.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_btnDescontoActionPerformed

