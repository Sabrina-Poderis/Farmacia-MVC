    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
            
            
            //cria um objeto
            Medicamento m = new Medicamento();

            //joga o codigo a ser pesquisado para o obj filme
            m.setId(txtIdCb.getText());

            // faz a procura
            m = DAOmed.buscar(m);

            if(m != null) {
            lblIum.setText(m.getId());
            lblNome.setText(m.getNome());
            lblValor.setText(Double.toString(m.getValor()));
            
            }

            else {
                JOptionPane.showMessageDialog(this,
                    "Medicamento não encontrado!",
                    "Mensagem ao Usuário",
                    JOptionPane.INFORMATION_MESSAGE);
            }

        } catch (SQLException ex) {
            Logger.getLogger(FormClienteEspecial.class.getName()).log(Level.SEVERE, null, ex);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(FormClienteEspecial.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

